#include "libft.h"

void	ft_putnbr(int n)
{
	int		i;

	i = 1;
	if (n < 0)
	{
		ft_putchar('-');
		if (n == -2147483648)
		{
			ft_putchar('2');
			n = -147483648;
		}
		n = -n;
	}
	while (n / i >= 10)
		i = i * 10;
	while (i > 1)
	{
		ft_putchar((n / i) + '0');
		n = n % i;
		i = i / 10;
	}
	ft_putchar(n + '0');
}
